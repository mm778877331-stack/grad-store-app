class User {
  final int idUser;
  final String name;
  final String email;
  final String phone;
  final String shopname;
  final String location;
  final int latitude;
  final int longitude;

  User({
    required this.idUser,
    required this.name,
    required this.email,
    required this.phone,
    required this.shopname, 
    required this.location, 
    required this.latitude, 
    required this.longitude,
  });
}
